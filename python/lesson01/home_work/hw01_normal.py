def isint(n):
    try:
        int(n)
        return True
    except ValueError:
        return False


# Задача-1: Дано произвольное целое число, вывести самую большую цифру этого числа.
# Например, дается x = 58375.
# Нужно вывести максимальную цифру в данном числе, т.е. 8.
# Подразумевается, что мы не знаем это число заранее.
# Число приходит в виде целого беззнакового.
# Подсказки:
# * постарайтесь решить задачу с применением арифметики и цикла while;
# * при желании и понимании решите задачу с применением цикла for.


def maxnumber():
    # В цикле
    while True:
        num = input('Введите число: ')
        if isint(num):
            # Если это число
            num_list = list(num)
            # Переводим строку в список
            mx = 0
            for item in num_list:
                # Приводим элемент списка к целочисленному значению
                item = int(item)
                # Если данный элемент больше максимального, делаем его максимальным
                if item > mx:
                    mx = item
            # Выводим максимальную цифру числа
            print('Самое большая цифра в введенном числе: ', mx)
            break
        else:
            # Выводим ошибку
            print('Вы ввели не число')

