# math.sqrt(4) - вычисляет корень числа 4

def quadratic_equation():
    while True:
        a = input('Введите коэффициент "а" квадратного уравнения: ')
        b = input('Введите коэффициент "b" квадратного уравнения: ')
        c = input('Введите коэффициент "c" квадратного уравнения: ')

        if isint(a) and isint(b) and isint(c):
            a = int(a)
            b = int(b)
            c = int(c)
            if a != 0 and b == 0 and c != 0:
                xx = (c / a)
                if -xx < 0:
                    print('Уравнение не имеет решения')
                    break
                else:
                    x = math.sqrt(-xx)
                    print('Корни уравнения:\n'
                          'x1 = ', round(x, 3), '\n'
                          'x2 = ', round(-x, 3))
                    break
            elif a != 0 and b != 0 and c == 0:
                x = (b / a)
                print('Корни уравнения:\n'
                      'x1 = 0\n'
                      'x2 = ', round(-x, 3))
                break
            elif a != 0 and b == 0 and c == 0:
                print('Корень данного уравнения равен 0')
                break
            else:
                d = b**2 - 4 * a * c
                if d < 0:
                    print('Уравнение не имеет решения')
                    break
                elif d == 0:
                    x = -b / (2 * a)
                    print('Корень данного уравнения равен ', x)
                    break
                else:
                    x1 = (-b + math.sqrt(d)) / (2 * a)
                    x2 = (-b - math.sqrt(d)) / (2 * a)
                    print('Корни уравнения:\n'
                          'x1 = ', round(x1, 3), '\n'
                          'x2 = ', round(x2, 3))
                    break
        else:
            # Выводим ошибку
            print('Вы ввели не число')


