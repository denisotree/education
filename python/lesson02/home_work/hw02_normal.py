def rm_duplicate():
    lst_in = [(random.randint(1, 15)) for num in range(10)]
    lst_out_1 = list(set(lst_in))
    lst_out_2 = []
    for i in lst_in:
        if lst_in.count(i) == 1:
            lst_out_2.append(i)
    print('Исходный список: ', lst_in)
    print('Список без дубликатов: ', lst_out_1)
    print('Список без повторений: ', lst_out_2)


