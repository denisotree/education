# Задача-1:
# Дан список, заполненный произвольными целыми числами, получите новый список,
# элементами которого будут квадратные корни элементов исходного списка,
# но только если результаты извлечения корня не имеют десятичной части и
# если такой корень вообще можно извлечь
# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]


def change_list():
    # Генерируем произвольный список из 10 элементов
    a_list = [(random.randint(-9, 36)) for num in range(10)]

    b_list = []

    for i in range(len(a_list)):  # Перебираем первый список и создаем новый на основе измененных элементов исходного
        if a_list[i] > 0 and math.sqrt(a_list[i]).is_integer():
            b_list.append(int(math.sqrt(a_list[i])))
        else:
            continue

    print('Исходный список: ', a_list)  # Выводим первый список
    print('Новый список: ', b_list)  # Выводим второй список


